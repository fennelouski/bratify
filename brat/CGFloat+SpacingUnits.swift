import UIKit

/// Extension on `CGFloat` to provide consistent spacing units based on a base unit.
extension CGFloat {
    /// The base unit for all spacing units.
    private static let baseUnit: CGFloat = 8

    /// One spacing unit (su).
    static var su: CGFloat { baseUnit * 1 }

    /// Two spacing units (su2).
    static var su2: CGFloat { baseUnit * 2 }

    /// Three spacing units (su3).
    static var su3: CGFloat { baseUnit * 3 }

    /// Four spacing units (su4).
    static var su4: CGFloat { baseUnit * 4 }

    /// Five spacing units (su5).
    static var su5: CGFloat { baseUnit * 5 }

    /// Six spacing units (su6).
    static var su6: CGFloat { baseUnit * 6 }

    /// Seven spacing units (su7).
    static var su7: CGFloat { baseUnit * 7 }

    /// Eight spacing units (su8).
    static var su8: CGFloat { baseUnit * 8 }

    /// Nine spacing units (su9).
    static var su9: CGFloat { baseUnit * 9 }

    /// Ten spacing units (su10).
    static var su10: CGFloat { baseUnit * 10 }

    /// Eleven spacing units (su11).
    static var su11: CGFloat { baseUnit * 11 }

    /// Twelve spacing units (su12).
    static var su12: CGFloat { baseUnit * 12 }

    /// Thirteen spacing units (su13).
    static var su13: CGFloat { baseUnit * 13 }

    /// Fourteen spacing units (su14).
    static var su14: CGFloat { baseUnit * 14 }

    /// Fifteen spacing units (su15).
    static var su15: CGFloat { baseUnit * 15 }

    /// Sixteen spacing units (su16).
    static var su16: CGFloat { baseUnit * 16 }

    /// Seventeen spacing units (su17).
    static var su17: CGFloat { baseUnit * 17 }

    /// Eighteen spacing units (su18).
    static var su18: CGFloat { baseUnit * 18 }

    /// Nineteen spacing units (su19).
    static var su19: CGFloat { baseUnit * 19 }

    /// Twenty spacing units (su20).
    static var su20: CGFloat { baseUnit * 20 }

    /// Twenty-one spacing units (su21).
    static var su21: CGFloat { baseUnit * 21 }

    /// Twenty-two spacing units (su22).
    static var su22: CGFloat { baseUnit * 22 }

    /// Twenty-three spacing units (su23).
    static var su23: CGFloat { baseUnit * 23 }

    /// Twenty-four spacing units (su24).
    static var su24: CGFloat { baseUnit * 24 }

    /// Ten spacing units (su25).
    static var su25: CGFloat { baseUnit * 25 }

    /// Twenty-six spacing units (su26).
    static var su26: CGFloat { baseUnit * 26 }

    /// Twenty-seven spacing units (su27).
    static var su27: CGFloat { baseUnit * 27 }

    /// Twenty-eight spacing units (su28).
    static var su28: CGFloat { baseUnit * 28 }

    /// Twenty-nine spacing units (su29).
    static var su29: CGFloat { baseUnit * 29 }

    /// Thirty spacing units (su30).
    static var su30: CGFloat { baseUnit * 30 }

    /// Thirty-one spacing units (su31).
    static var su31: CGFloat { baseUnit * 31 }

    /// Thirty-two spacing units (su32).
    static var su32: CGFloat { baseUnit * 32 }

    /// Thirty-three spacing units (su33).
    static var su33: CGFloat { baseUnit * 33 }

    /// Thirty-four spacing units (su34).
    static var su34: CGFloat { baseUnit * 34 }

    /// Thirty-five spacing units (su35).
    static var su35: CGFloat { baseUnit * 35 }

    /// Ten spacing units (su36).
    static var su36: CGFloat { baseUnit * 36 }

    /// Thirty-seven spacing units (su37).
    static var su37: CGFloat { baseUnit * 37 }

    /// Thirty-eight spacing units (su38).
    static var su38: CGFloat { baseUnit * 38 }

    /// Thirty-nine spacing units (su39).
    static var su39: CGFloat { baseUnit * 39 }

    /// Forty spacing units (su40).
    static var su40: CGFloat { baseUnit * 40 }

    /// Ten spacing units (su64).
    static var su64: CGFloat { baseUnit * 64 }

    /// Half a spacing unit (suHalf).
    static var suHalf: CGFloat { baseUnit * 0.5 }
}
